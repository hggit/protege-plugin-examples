package com.similar2.matcher.ontology.model.exceptions;

/**
 * Root of runtime exceptions classes for Aristotelian Ontologies
 * @author Philippe Genoud (Philippe.Genoud@imag.fr)
 */
public class AristotelianOntologyRTException extends
		RuntimeException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 9112365532321202521L;

	/**
	 * 
	 */
	public AristotelianOntologyRTException() {
		super();
	}

	/**
	 * @param mess
	 */
	public AristotelianOntologyRTException(String mess) {
		super(mess);
	}

	/**
	 * @param mess
	 * @param cause
	 */
	public AristotelianOntologyRTException(String mess, Throwable cause) {
		super(mess, cause);
	}

}


